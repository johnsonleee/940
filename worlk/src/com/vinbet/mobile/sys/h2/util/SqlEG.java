package com.vinbet.mobile.sys.h2.util;

import java.util.ArrayList;
import java.util.List;

/**
 * SQL条件组
 * 
 * @author ray
 * 
 */
public class SqlEG {

	private List<SqlE> sqlEs = new ArrayList<SqlE>();
	private String sign;

	public void add(SqlE sqle) {
		sqlEs.add(sqle);
	}

	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}

	public List<SqlE> get() {
		return sqlEs;
	}

	public boolean isEmpty() {
		return sqlEs.isEmpty();
	}

}
