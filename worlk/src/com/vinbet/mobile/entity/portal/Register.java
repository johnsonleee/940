package com.vinbet.mobile.entity.portal;

import com.vinbet.mobile.sys.h2.anno.Column;
import com.vinbet.mobile.sys.h2.anno.Table;

/**
 * 用户注册DTO
 * 
 * @author jagger
 * 
 */
@Table
public class Register {
	/**
	 * 用户账号
	 */
	@Column
	private String userName;
	/**
	 * 用户真实姓名
	 */
	@Column
	private String trueName;
	/**
	 * 用户密码
	 */
	private String password;
	/**
	 * 确认密码
	 */
	private String passwd;
	/**
	 * 个人邮箱
	 */
	@Column
	private String email;
	/**
	 * 手机号码
	 */
	@Column
	private String mobile;
	/**
	 * 所属代理账号
	 */
	@Column
	private String agentName;
	/**
	 * 货币类型:1人民币、2泰铢
	 */
	@Column
	private String currenytype;
	/**
	 * 同意条款
	 */
	private String agree;
	/**
	 * 安全码
	 */
	private String securityCode;
	/**
	 * 域名地址
	 */
	@Column
	private String domainAddr;
	/**
	 * 注册的IP
	 */
	@Column
	private String registeredIp;
	/**
	 * 注册时间
	 */
	@Column
	private String joinDate;
	/**
	 * 国籍
	 */
	
	private String country;
	
	/**
	 * 性别
	 */
	
	private String sex;
	
	/**
	 * 生日
	 */
	
	private String birthdate;
	
	/**
	 * 手机号码前缀
	 */
	
	private String phoneFix;
	
	/**
	 * 手机号码
	 */
	
	private String phoneNum;
	
	/**
	 * 邮箱地址
	 */
	private String mailAddress;
	
	/**
	 * 验证类型 1、手机 2、邮箱
	 */
	private String sendType;
	/**
	 * 手机验证码
	 */
	private String phoneCode;
	/**
	 * 邮件验证码
	 */
	private String mailCode;
	
	/**
	 * 用户ID
	 */
	private String userid;
	
	
	/**
	 * 姓
	 */
	private String surName;
	
	
	/**
	 * 名
	 */
	private String givenName;
	/**
	 * 是否加密方式
	 */
	private boolean pwdRsaFlag;
	

	public boolean isPwdRsaFlag() {
		return pwdRsaFlag;
	}

	public void setPwdRsaFlag(boolean pwdRsaFlag) {
		this.pwdRsaFlag = pwdRsaFlag;
	}

	public String getDomainAddr() {
		return domainAddr;
	}

	public void setDomainAddr(String domainAddr) {
		this.domainAddr = domainAddr;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getTrueName() {
		return trueName;
	}

	public void setTrueName(String trueName) {
		this.trueName = trueName;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getAgentName() {
		return agentName;
	}

	public void setAgentName(String agentName) {
		this.agentName = agentName;
	}

	public String getCurrenytype() {
		return currenytype;
	}

	public void setCurrenytype(String currenytype) {
		this.currenytype = currenytype;
	}

	public String getPasswd() {
		return passwd;
	}

	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}

	public String getAgree() {
		return agree;
	}

	public void setAgree(String agree) {
		this.agree = agree;
	}

	public String getSecurityCode() {
		return securityCode;
	}

	public void setSecurityCode(String securityCode) {
		this.securityCode = securityCode;
	}

	public String getRegisteredIp() {
		return registeredIp;
	}

	public void setRegisteredIp(String registeredIp) {
		this.registeredIp = registeredIp;
	}

	public String getJoinDate() {
		return joinDate;
	}

	public void setJoinDate(String joinDate) {
		this.joinDate = joinDate;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getBirthdate() {
		return birthdate;
	}

	public void setBirthdate(String birthdate) {
		this.birthdate = birthdate;
	}

	public String getPhoneFix() {
		return phoneFix;
	}

	public void setPhoneFix(String phoneFix) {
		this.phoneFix = phoneFix;
	}

	public String getPhoneNum() {
		return phoneNum;
	}

	public void setPhoneNum(String phoneNum) {
		this.phoneNum = phoneNum;
	}

	public String getMailAddress() {
		return mailAddress;
	}

	public void setMailAddress(String mailAddress) {
		this.mailAddress = mailAddress;
	}

	public String getSendType() {
		return sendType;
	}

	public void setSendType(String sendType) {
		this.sendType = sendType;
	}

	public String getPhoneCode() {
		return phoneCode;
	}

	public void setPhoneCode(String phoneCode) {
		this.phoneCode = phoneCode;
	}

	public String getMailCode() {
		return mailCode;
	}

	public void setMailCode(String mailCode) {
		this.mailCode = mailCode;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getSurName() {
		return surName;
	}

	public void setSurName(String surName) {
		this.surName = surName;
	}

	public String getGivenName() {
		return givenName;
	}

	public void setGivenName(String givenName) {
		this.givenName = givenName;
	}
	
	
}
