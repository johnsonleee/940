public class WalletCategory {
	
	private String walletCategory; //钱包类别标识
	private String walletName; //钱包中文名
	private String iid;        //钱包国际化ID
	private String appMark;      //应用标识码
	private String singleInLimit; //单笔划入限额
	private String singleOutLimit; //单笔划出限额
	private String dayInLimit;  //单日划入限额
	private String dayOutLimit;//单日划出限额
	private String currency;   //钱包支持的货币，用逗号分隔
	private String status;  //1正常，0维护
	private String walletType; //0内部，1外部
	private String fundsDetailUrl;  //资金明细查询地址
	
	public String getWalletCategory() {
		return walletCategory;
	}
	public void setWalletCategory(String walletCategory) {
		this.walletCategory = walletCategory;
	}
	public String getWalletName() {
		return walletName;
	}
	public void setWalletName(String walletName) {
		this.walletName = walletName;
	}
	public String getIid() {
		return iid;
	}
	public void setIid(String iid) {
		this.iid = iid;
	}
	public String getAppMark() {
		return appMark;
	}
	public void setAppMark(String appMark) {
		this.appMark = appMark;
	}
	public String getSingleInLimit() {
		return singleInLimit;
	}
	public void setSingleInLimit(String singleInLimit) {
		this.singleInLimit = singleInLimit;
	}
	public String getSingleOutLimit() {
		return singleOutLimit;
	}
	public void setSingleOutLimit(String singleOutLimit) {
		this.singleOutLimit = singleOutLimit;
	}
	public String getDayInLimit() {
		return dayInLimit;
	}
	public void setDayInLimit(String dayInLimit) {
		this.dayInLimit = dayInLimit;
	}
	public String getDayOutLimit() {
		return dayOutLimit;
	}
	public void setDayOutLimit(String dayOutLimit) {
		this.dayOutLimit = dayOutLimit;
	}
	public String getCurrency() {
		return currency;
	}
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getWalletType() {
		return walletType;
	}
	public void setWalletType(String walletType) {
		this.walletType = walletType;
	}
	public String getFundsDetailUrl() {
		return fundsDetailUrl;
	}
	public void setFundsDetailUrl(String fundsDetailUrl) {
		this.fundsDetailUrl = fundsDetailUrl;
	}
	
	 

}
